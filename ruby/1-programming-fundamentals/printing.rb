puts "Hello World"  # Use 'puts' to print

puts 1 + 5  # Add 1 and 5, then print out the result

x = 7 # Assign x to have the value of 7

puts x  # Print out the value of x
puts x * 2  # Print out the value of x multiplied by 2

ab = 20 # Assign value of 20 to ab
ab = 5  # Overwrite value of ab to 5

# Interpolation
puts ab
puts "ab: #{ab}"  # Double quotes allows you to resolve value of variable into a String
puts "ab"
puts 'ab'
puts 'ab: #{ab}'  # Single quotes does not allow interpolation
